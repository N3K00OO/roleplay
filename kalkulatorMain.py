class Calculator:
    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, c, d):
        return c / d

    def modulo(self, a, b):
        return a % b

    def power(self, a, b):
        return a ** b