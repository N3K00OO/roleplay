class Calculator:
    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, x, y):
        return x * y

    def divide(self, a, b):
        return a / b

    def modulo(self, a, b):
        return a % b

    def power(self, a, b):
        return a ** b